#ifndef ZIM_BZIP2_H
#define ZIM_BZIP2_H

#include <stdexcept>

namespace zim
{
  class Bzip2Error : public std::runtime_error
  {
      int zRet;

    public:
      Bzip2Error(int zRet_, const std::string& msg)
        : std::runtime_error(msg),
          zRet(zRet_)
          { }

      int getRet() const  { return zRet; }
      static const char* getErrorString(int ret);
  };

}

#endif // ZIM_BZIP2_H

